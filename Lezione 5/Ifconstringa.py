# Confronto fra due stringhe
stringa1 = "Ciao"
stringa2 = "Manu"
stringa3 = "Fari"

if stringa1 == stringa2:
    print(stringa1, "e' uguale a", stringa2)
elif stringa1 == stringa3:
    print(stringa1, "e' uguale a", stringa3)
elif stringa2 == stringa3:
    print(stringa2, "e' uguale a", stringa3)
else:
    print("Non ci sono stringhe uguali")